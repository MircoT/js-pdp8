// Fibonacci
ORG 100

LDA SIZE
INC
INC
STA SIZE

LOOP, LDA CUR I
	STA TMP

	LDA CUR
	INC
	STA CUR
	
	LDA CUR I
	ADD TMP
	STA TMP
	
	LDA CUR
	INC
	STA CUR
	
	LDA TMP
	STA CUR I

	LDA CUR
	CMA
	INC
	CMA
	STA CUR
	
	ISZ SIZE
	BUN LOOP

HLT

TMP, DEC 0

V, DEC 1
DEC 1
DEC -1
DEC -1
DEC -1
DEC -1

CUR, V
SIZE, DEC -6

END